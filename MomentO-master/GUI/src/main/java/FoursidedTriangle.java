    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
         double x1,x2,x3,y1,y2,y3;
    x1 = Float.parseFloat(jTextField9.getText());
    x2 = Float.parseFloat(jTextField10.getText());
    x3 = Float.parseFloat(jTextField11.getText());
    y1 = Float.parseFloat(jTextField12.getText());
    y2 = Float.parseFloat(jTextField13.getText());
    y3 = Float.parseFloat(jTextField14.getText());
    double ox = Float.parseFloat(jTextField31.getText());
    double oy = Float.parseFloat(jTextField32.getText());
    
    double centroidX = (x1+x2+x3)/3;
    double centroidY = (y1+y2+y3)/3;
    double breadth = Math.sqrt((Math.pow((y2-y1),2)+Math.pow((x2-x1),2)));
    double area = (x1*(y2-y3)+x2*(y3-y1)+x3*(y1-y2))/2;
    double height = (2*area)/breadth;
    double a = x3-x1;
    double Ixc = (breadth*Math.pow(height,3))/36;
    double Iyc = (Math.pow((breadth),3)*height-Math.pow((breadth),2)*height*a+breadth*height*Math.pow((a),2))/36;
    double Ix = Ixc + area*Math.pow((centroidY-oy),2);
    double Iy = Iyc + area*Math.pow((centroidX-ox),2);
    
    
    jTextField25.setText(String.valueOf(Ix));
    jTextField26.setText(String.valueOf(Iy));
    }//GEN-LAST:event_jButton6ActionPerformed

